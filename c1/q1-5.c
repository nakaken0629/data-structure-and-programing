#include <stdio.h>

int main() {
    int i, j, k;
    int a[3][4][2] = {
        {
            {100, 101},
            {200, 201},
            {300, 301},
            {400, 401}
        },
        {
            {500, 501},
            {600, 601},
            {700, 701},
            {800, 801}
        },
        {
            {900, 901},
            {1000, 1001},
            {1100, 1101},
            {1200, 1201}
        }
    };

    for (i = 0; i < 3; i++) {
        for (j = 0; j < 4; j++) {
            for (k = 0; k < 2; k++) {
                printf("array[%d][%d][%d]=%4d\n", i, j, k, a[i][j][k]);
            }
        }
    }
    
    return 0;
}
